				  unsigned int length;
				  uint8_t buffer[FLASH_PAGE_SIZE];
				  uint32_t address;

				  for (address = 0; address < BOOTLOADER_SIZE; address += FLASH_PAGE_SIZE) {
					  memcpy((void *) buffer, (void *) (FLASH_BASE + address), 2048);
				  	  if ((result = f_write(&SDFile, (void *) buffer, FLASH_PAGE_SIZE, &length)) != FR_OK) {
					  	  printf("Failed to write data to Bootload.bin: %d\n", result);
					  	  break;
				  	  }
				  }
				  f_close(&SDFile);
				  if (f_stat("BOOTLOAD.BIN", &FileInfo) == FR_OK) {
					  printf("Bootload.bin created (%ld)\n", FileInfo.fsize);
				  }
